    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        DefaultTableModel model = (DefaultTableModel)tblUser.getModel();
        int baris = tblUser.rowAtPoint(evt.getPoint());

        String id =tblUser.getValueAt(baris, 0).toString();
        txtId.setText(id);
        String username = tblUser.getValueAt(baris, 1).toString();
        txtUsername.setText(username);
        String nama = tblUser.getValueAt(baris, 2).toString();
        txtNama.setText(nama);
        String jenis_kelamin = tblUser.getValueAt(baris, 3).toString();
        cboJenisKelamin.setSelectedItem(jenis_kelamin);
        String alamat = tblUser.getValueAt(baris, 4).toString();
        txtAlamat.setText(alamat);
        String level = tblUser.getValueAt(baris, 5).toString();
        cboLevel.setSelectedItem(level);
        String password = tblUser.getValueAt(baris, 6).toString();
        txtPassword.setText(password);
    }//GEN-LAST:event_tblUserMouseClicked

