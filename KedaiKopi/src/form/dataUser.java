    public void autoId()
	{
            try
            {
                Connection konek = koneksi.koneksi();
                Statement state = konek.createStatement();
                String query = "SELECT MAX(id_profile) FROM user";//Mendapatkan nilai id terakhir

                ResultSet rs = state.executeQuery(query);
                if(rs.next()){
                    int a = rs.getInt(1);
                    txtId.setText(Integer.toString(a+1));//Nilai input yang terakhir ditambahkan 1
                }
                    rs.close();
                    state.close();
                }
            catch(Exception ex)
            {
                    System.out.println(ex);
            }
        }
    
    public void readTable()
        {
            // membuat tampilan model tabel
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID User");
            model.addColumn("Username");
            model.addColumn("Nama Lengkap");
            model.addColumn("Jenis Kelamin");
            model.addColumn("Alamat");
            model.addColumn("Level");
            model.addColumn("Password");

            try {
            String sql = "SELECT id_user, username, nama_lengkap, jenis_kelamin, alamat, level, password FROM user JOIN user_profile USING(id_profile)";
            java.sql.Connection conn=(Connection)koneksi.koneksi();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet rs=stm.executeQuery(sql);
            while(rs.next()){
            model.addRow(new Object[]{
                rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7)
                });
            }
            tblUser.setModel(model);
            
            tblUser.getColumnModel().getColumn(6).setMinWidth(0);
            tblUser.getColumnModel().getColumn(6).setMaxWidth(0);
            
            } catch (Exception e) {
            }
        }
