    private void tblBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBarangMouseClicked
        DefaultTableModel model = (DefaultTableModel)tblBarang.getModel();
        int baris = tblBarang.rowAtPoint(evt.getPoint());

        String id =tblBarang.getValueAt(baris, 0).toString();
        txtId.setText(id);
        String nama = tblBarang.getValueAt(baris, 1).toString();
        txtNama.setText(nama);
        String jumlah = tblBarang.getValueAt(baris, 2).toString();
        txtJumlah.setText(jumlah);
        String kondisi = tblBarang.getValueAt(baris, 3).toString();
        cboKondisi.setSelectedItem(kondisi);
    }//GEN-LAST:event_tblBarangMouseClicked

