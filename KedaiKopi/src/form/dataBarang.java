    public void autoId()
	{
            try
            {
                Connection konek = koneksi.koneksi();
                Statement state = konek.createStatement();
                String query = "SELECT MAX(id_barang) FROM barang";//Mendapatkan nilai id terakhir

                ResultSet rs = state.executeQuery(query);
                if(rs.next()){
                    int a = rs.getInt(1);
                    txtId.setText(Integer.toString(a+1));//Nilai input yang terakhir ditambahkan 1
                }
                    rs.close();
                    state.close();
                }
            catch(Exception ex)
            {
                    System.out.println(ex);
            }
        }
    
    public void readTable()
        {
            // membuat tampilan model tabel
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Barang");
            model.addColumn("Nama Barang");
            model.addColumn("Jumlah");
            model.addColumn("Kondisi");
            
            try {
            String sql = "SELECT * FROM barang";
            java.sql.Connection conn=(Connection)koneksi.koneksi();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet rs=stm.executeQuery(sql);
            while(rs.next()){
            model.addRow(new Object[]{
                rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)
                });
            }
            tblBarang.setModel(model);
            }catch (Exception e) {
            }
        }
    
